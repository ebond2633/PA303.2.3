public class Postfix {
    public static void main(String[] args) {
        // Declare an integer variable
        int number = 5;

        // Use the postfix increment operator to increase the value
        int incrementedValue = number++; // Post-increment

        // Print the value before and after the increment
        System.out.println("Original value: " + number); // Prints 6
        System.out.println("Incremented value: " + incrementedValue); // Prints 5
    }
}

