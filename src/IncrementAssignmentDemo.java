public class IncrementAssignmentDemo {
    public static void main(String[] args) {
        int count = 0; // Initialize count to 0
        System.out.println("Initial value: " + count);

        // Increment count using the += operator
        count += 1;
        System.out.println("After increment: " + count);

        // Increment count again
        count += 1;
        System.out.println("After second increment: " + count);
    }
}
