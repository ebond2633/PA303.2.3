public class BitwiseOperatorsExample {
    public static void main(String[] args) {
        // Given values
        int x = 5; // Binary: 0101
        int y = 7; // Binary: 0111

        // Bitwise AND operation
        int result = x & y; // Binary: 0101 & 0111 = 0101 (decimal: 5)

        // Print the result
        System.out.println("Bitwise AND of " + x + " and " + y + " = " + result);
    }
}

